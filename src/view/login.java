    private void bmasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bmasukActionPerformed
        String level ="";
        try {
            tusername.requestFocus();
            java.sql.Statement stat = conn.createStatement();
            ResultSet result=stat.executeQuery ("SELECT * FROM tbuser WHERE "
                + "username='" +tusername.getText()+"'");
            if (result.next()) {
                if (tpassword.getText().equals(result.getString("password"))){
                    JOptionPane.showMessageDialog(rootPane, "Selamat Datang "+tusername.getText());
                    mainmenu mm = new mainmenu();
                    mm.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane,"Password Salah");
                    tpassword.setText("");
                    tusername.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "User Tidak Ditemukan");
                tusername.setText("");
                tpassword.setText("");
                tusername.requestFocus();
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }//GEN-LAST:event_bmasukActionPerformed

