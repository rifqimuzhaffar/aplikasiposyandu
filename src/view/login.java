    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        
        String sql = "insert into tbuser values (?,?,?,?)";
        try{
            if (isUsernameAvailable(tregisuser.getText())) {
                if (tnamalengkap.getText().isEmpty() || tregisuser.getText().isEmpty() || tregispass.getText().isEmpty()
                    || tregisrepass.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Semua data harus terisi", "Notice !!!", JOptionPane.WARNING_MESSAGE);
                } else {
                    if (tregispass.getText().equals(tregisrepass.getText())) {
                        PreparedStatement stat = conn.prepareStatement(sql);
                        stat.setString(1, tnamalengkap.getText());
                        stat.setString(2, tregisuser.getText());
                        stat.setString(3, tregispass.getText());
                        stat.setString(4, tregisrepass.getText());
                        stat.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                        kosong();
                        tnamalengkap.requestFocus();
                        
                        panelmain.removeAll();
                        panelmain.repaint();
                        panelmain.revalidate();
                        //add panel
                        panelmain.add(panellogin);
                        panelmain.repaint();
                        panelmain.revalidate();
                    }else{
                        JOptionPane.showMessageDialog(null, "Password tidak sama. Harap periksa kembali.", "Notice !!!", JOptionPane.WARNING_MESSAGE);
                        tregispass.setText("");
                        tregisrepass.setText("");
                    }
                }
            }else {
                JOptionPane.showMessageDialog(null, "Username sudah digunakan", "Notice !!!", JOptionPane.WARNING_MESSAGE);
                tregisuser.setText("");
            }
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_bsimpanActionPerformed

